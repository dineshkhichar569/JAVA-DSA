package LeetCode;

import java.util.Arrays;

public class LC_Concatenation_of_Array {
    public static void main(String[] args) {
        int[] nums = {1, 2, 3, 4};

        int n = nums.length;
        while (n>0){
            System.out.println(Arrays.toString(nums));
            n--;
        }

    }
}
